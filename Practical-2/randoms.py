import random
print(random.randint(5, 20))  # line 1
# 5 was the smallest and 20 was the largest.

print(random.randrange(3, 10, 2))  # line 2
# 3 was the smallest and 9 was the largest. It could not produce a 2.

print(random.uniform(2.5, 5.5))  # line 3
# 2.9837237430007706 was the smallest and 4.926073725609541 was the largest.

